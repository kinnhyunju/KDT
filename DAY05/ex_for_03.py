# [실습] 출력하고 싶은 단을 입력 받아서 해당 단의 구구단을 출력하세요
# [출력 예시] 2 * 1 = 2
dan = int(input("숫자 입력 : "))
num = range(1,10)
for n in num:
    print(f'{dan} * {n} = {dan*n}')