# ------------------------------------------------------------------
# List 데이터 자료형 살펴보기
# - List와 str
# -----------------------------------------------------------------
msg = "Happy"

# str => list 형변환
datas = list(msg)       # 알파벳 하나씩 쪼개짐
print(datas)

msg = ['Happy']
datas = list(msg)       # 0번 원소 1개로 인식해서 쪼개지지 않음
print(datas)