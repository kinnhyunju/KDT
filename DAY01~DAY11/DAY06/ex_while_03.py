## [실습] 3단 출력하기 단 while문 사용
n = 1
while n<10:
    print(f'3 * {n} = {3*n}')
    n=n+1

# ------------------------------------------------------------------------------------
## [실습] 1~30 범위의 수 중에서 홀수만 출력
##        단 while문 사용
# ------------------------------------------------------------------------------------
## [1] 1~30 숫자 while문으로 출력
num=1
while num<31:
    print(num)
    num=num+1

## [실습] 1~30 범위의 수 중에서 홀수만 출력 - 방법 1
n=1
while n<31:
    if n%2:
        print(n)
    n=n+1

## [실습] 1~30 범위의 수 중에서 홀수만 출력 - 방법2
m=1
while m<31:
    print(m)
    m=m+2