# [실습] 2단 ~ 9단까지 모두 출력하세요
# -------------------------------------------------------------------------------------------
dan = range(2,10)
num = range(1,10)
# num = [1,2,3,4,5,6,7,8,9] 로 써도 됨
# 따로 변수 만들지 않고 d in range(2,10) / n in range(1,10) 으로 써도 됨!
for d in dan : 
    for n in num:
        print(f'{d} * {n} = {d*n}')
    print('-'*20)

